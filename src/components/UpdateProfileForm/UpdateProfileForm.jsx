const UpdateProfileForm = ({ onUpdate }) => {
  const { userId } = useParams();
  const navigate = useNavigate();
  const [formData, setFormData] = useState({
    name: "",
    email: "",
    phoneNumber: "",
    birthdate: "",
    country: "",
    profilePicture: null,
  });
  const [error, setError] = useState("");
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchUserData = async () => {
      try {
        setLoading(true);
        const user = await getCurrentUser();
        console.log("Fetched User Data:", user);

        if (!user || user._id !== userId) {
          setError("User not found or unauthorized to edit this profile.");
          setLoading(false);
          return;
        }

        setFormData({
          name: user.name || "",
          email: user.email || "",
          phoneNumber: user.phoneNumber || "",
          birthdate: user.birthdate ? new Date(user.birthdate).toISOString().split("T")[0] : "",
          country: user.country || "",
          profilePicture: null,
        });
      } catch (error) {
        console.error("Error fetching user data:", error);
        setError("Failed to fetch user data. Please try again.");
      } finally {
        setLoading(false);
      }
    };

    fetchUserData();
  }, [userId]);

  const handleChange = (e) => {
    setFormData({ ...formData, [e.target.name]: e.target.value });
    setError("");
  };

  const handleFileChange = (e) => {
    const selectedFile = e.target.files[0];
    if (!selectedFile) {
      setError("No file selected.");
      return;
    }
    if (!selectedFile.type.startsWith("image/")) {
      setError("Invalid file type: Only images allowed.");
      return;
    }
    setError("");
    setFormData({ ...formData, profilePicture: selectedFile });
  };

  const handleCountryChange = (selectedOption) => {
    setFormData({ ...formData, country: selectedOption ? selectedOption.value : "" });
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    try {
      const formDataToSend = new FormData();
      formDataToSend.append("name", formData.name);
      formDataToSend.append("email", formData.email);
      formDataToSend.append("phoneNumber", formData.phoneNumber);
      formDataToSend.append("birthdate", formData.birthdate);
      formDataToSend.append("country", formData.country);
      if (formData.profilePicture) {
        const reader = new FileReader();
        const base64Promise = new Promise((resolve) => {
          reader.onload = () => resolve(reader.result);
          reader.readAsDataURL(formData.profilePicture);
        });
        const base64Image = await base64Promise;
        formDataToSend.append("profilePicture", base64Image);
        console.log("Base64 image length:", base64Image.length); // Log size
      }

      console.log("Submitting form data:", Object.fromEntries(formDataToSend));
      const updatedUser = await updateUserProfile(formDataToSend);
      if (onUpdate) {
        onUpdate(updatedUser);
      }
      navigate("/profile");
    } catch (error) {
      setError("Error updating profile. Please try again.");
      console.error("Error updating profile:", error);
    }
  };

  if (loading) return <p className="loading-message">Loading user data...</p>;
  if (error) return <p className="error-message">{error}</p>;

  return (
    <div className="update-profile-container">
      <h2 className="form-heading">Update Your Profile</h2>
      <form className="update-profile-form" onSubmit={handleSubmit}>
        <div className="form-group">
          <label>Name:</label>
          <input type="text" name="name" value={formData.name} onChange={handleChange} />
        </div>
        <div className="form-group">
          <label>Email:</label>
          <input type="email" name="email" value={formData.email} onChange={handleChange} />
        </div>
        <div className="form-group">
          <label>Phone Number:</label>
          <input
            type="text"
            name="phoneNumber"
            value={formData.phoneNumber}
            onChange={handleChange}
            placeholder="+12025550123"
          />
        </div>
        <div className="form-group">
          <label>Birthdate:</label>
          <input type="date" name="birthdate" value={formData.birthdate} onChange={handleChange} max="2025-03-02" />
        </div>
        <div className="form-group">
          <label>Country:</label>
          <Select
            options={countries}
            value={countries.find((c) => c.value === formData.country)}
            onChange={handleCountryChange}
            className="country-select"
            classNamePrefix="select"
          />
        </div>
        <div className="form-group">
          <label>Profile Picture:</label>
          <input type="file" name="profilePicture" accept="image/*" onChange={handleFileChange} />
        </div>
        {error && <p className="error-message">{error}</p>}
        <button type="submit" className="submit-button">Update Profile</button>
      </form>
    </div>
  );
};

export default UpdateProfileForm;




